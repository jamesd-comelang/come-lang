#ifndef COME_TYPES_H
#define COME_TYPES_H

#include <stdint.h>

// COME language type aliases
typedef int8_t byte;
typedef int8_t i8;
typedef uint8_t ubyte;
typedef uint8_t u8;
typedef int16_t i16;
typedef uint16_t ushort;
typedef uint16_t u16;
typedef int32_t i32;
typedef uint32_t uint;
typedef uint32_t u32;
typedef int64_t i64;
typedef uint64_t ulong;
typedef uint64_t u64;
typedef float f32;
typedef double f64;
typedef int32_t wchar;

#endif // COME_TYPES_H
